class Player:
  name: str
  points: int

  def __init__(self, name, points):
      self.name = name
      self.points = 0
      
      def get_player_points(self):
        self.points
      
      def set_player_points(self):
        self.points = points

      def get_player_name(self):
        self.name

      def set_player_name(self):
        self.name = name

      def increase_points(self):
        self.points += 1

      def decrease_points(self):
        self.points -= 1